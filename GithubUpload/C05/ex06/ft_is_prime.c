int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb <= 1)
		return (0);
	else
	{
		while (i < nb)
		{
			if (!(nb % i == 0))
				i++;
			else
				return (0);
		}
	}
	return (1);
}
