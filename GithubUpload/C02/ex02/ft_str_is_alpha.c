int	ft_str_is_alpha(char *str)
{
	int	i;
	int	k;

	i = 1;
	k = 0;
	if (str[0] == '\0')
		return (1);
	while (str[k] != '\0')
	{
		if (!(str[k] >= 'a' && str[k] <= 'z')
			&& !(str[k] >= 'A' && str[k] <= 'Z'))
		{
			i = 0;
			break ;
		}
		k++;
	}
	return (i);
}
