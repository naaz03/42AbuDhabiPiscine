#include <unistd.h>

void	print_array(int i, int j)
{
	char	res[5];

	res[0] = '0' + (i / 10) % 10;
	res[1] = '0' + i % 10;
	res[2] = ' ';
	res[3] = '0' + (j / 10) % 10;
	res[4] = '0' + j % 10;
	write(1, &res, 5);
}

void	ft_print_comb2(void)
{
	int		a[2];

	a[0] = 00;
	a[1] = 01;
	while (a[0] < 99)
	{
		while (a[1] <= 99)
		{
			print_array (a[0], a[1]);
			if (a[0] == 98 && a[1] == 99)
			{
			}
			else
			{
				write(1, ", ", 2);
			}							
			a[1]++;
		}		
		a[0]++;
		a[1] = a[0] + 1;
	}
}
